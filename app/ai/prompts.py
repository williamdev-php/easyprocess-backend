def build_freeform_prompt(
    blueprint: "SiteBlueprint",
    business_name: str,
    context: str,
    email: str | None,
    colors: dict | None,
    logo_url: str | None,
    images: list | None = None,
    font: str | None = None,
) -> tuple[str, str]:
    """Build (system_prompt, user_prompt) for the FREE-FORM flow (no scraping)."""

    blueprint_json = json.dumps(blueprint.model_dump(), ensure_ascii=False, indent=2)

    system_prompt = _BLUEPRINT_SYSTEM_TEMPLATE.format(
        blueprint_json=blueprint_json,
        compact_schema=_COMPACT_SCHEMA,
    )

    # --- Build user prompt ---
    images_summary = _format_images_list(images)
    css_colors = _format_colors(colors)

    sections_detail_parts: list[str] = []
    for sp in blueprint.sections:
        sections_detail_parts.append(
            f"  {sp.priority}. [{sp.section}] — {sp.tip}"
        )
    sections_detail = "\n".join(sections_detail_parts)

    font_line = f"\nTypsnitt: {_sanitize_for_prompt(font, 100)}" if font else ""

    color_section = ""
    if colors:
        color_section = f"""
═══════════════════════════════════════
FÄRGPALETT
═══════════════════════════════════════
{css_colors}
Blueprintens färgriktning: {blueprint.color_direction or "Använd färgerna ovan"}"""

    user_prompt = f"""Skapa en komplett hemsida i JSON-format.

═══════════════════════════════════════
SYFTE & TON
═══════════════════════════════════════
Syfte: {_sanitize_for_prompt(blueprint.purpose, 300)}
Ton: {_sanitize_for_prompt(blueprint.tone, 200)}
Målgrupp: {_sanitize_for_prompt(blueprint.target_audience, 200)}

═══════════════════════════════════════
FÖRETAG & BESKRIVNING
═══════════════════════════════════════
Namn: {_sanitize_for_prompt(business_name, 200) if business_name else "Okänt företag"}
Email: {_sanitize_for_prompt(email, 200) if email else "Ej angiven"}

Kundens beskrivning:
{_sanitize_for_prompt(context, 2000)}

═══════════════════════════════════════
SEKTIONER ATT SKAPA (med tips)
═══════════════════════════════════════
{sections_detail}

Exkluderade sektioner (skapa INTE dessa): {', '.join(blueprint.excluded_sections) if blueprint.excluded_sections else "Inga"}

Innehållsriktning: {_sanitize_for_prompt(blueprint.content_direction, 500)}
{color_section}

═══════════════════════════════════════
TILLGÄNGLIGA BILDER
═══════════════════════════════════════
{images_summary}

═══════════════════════════════════════
GENERERA JSON

Logotyp: {logo_url or ""}{font_line}
Svara med ENBART valid JSON. Följ blueprintens sektioner och tips. Generera allt innehåll baserat på kundens beskrivning ovan."""

    return system_prompt, user_prompt
