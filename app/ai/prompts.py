        if texts.get("team_members"):
            tm_parts = []
            for tm in texts["team_members"][:8]:
                name = _sanitize_for_prompt(tm.get("name", ""), 100)
                role = _sanitize_for_prompt(tm.get("role", ""), 100)
                tm_parts.append(f"- {name} ({role})" if role else f"- {name}")
            if tm_parts:
                team_summary = "\n".join(tm_parts)

