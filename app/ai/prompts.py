        if texts.get("paragraphs"):
            p_parts = []
            for p in texts["paragraphs"][:20]:
                p_parts.append(f"- {_sanitize_for_prompt(p, 500)}")
            if p_parts:
                paragraphs_summary = "\n".join(p_parts)

