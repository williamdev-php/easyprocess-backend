    faq_summary = "Ingen FAQ hittad."
    team_summary = "Inga teammedlemmar hittade."

    if texts:
        if texts.get("title"):
            page_title = _sanitize_for_prompt(texts["title"], 300)
        if texts.get("hero_text"):
            hero_text = _sanitize_for_prompt(texts["hero_text"], 300)
        if texts.get("hero_subtitle"):
            hero_subtitle = _sanitize_for_prompt(texts["hero_subtitle"], 500)
        if texts.get("description"):
            meta_description = _sanitize_for_prompt(texts["description"], 500)
        if texts.get("about"):
            about_text = _sanitize_for_prompt(texts["about"], 2000)

        # All headings
        if texts.get("headings"):
            h_parts = []
            for h in texts["headings"][:20]:
                h_parts.append(f"[{h['level']}] {_sanitize_for_prompt(h['text'], 200)}")
            if h_parts:
                headings_summary = "\n".join(h_parts)

