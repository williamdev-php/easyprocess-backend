            for fq in texts["faq_items"][:10]:
                q = _sanitize_for_prompt(fq.get("question", ""), 200)
                a = _sanitize_for_prompt(fq.get("answer", ""), 500)
                faq_parts.append(f"Q: {q}\nA: {a}")
            if faq_parts:
                faq_summary = "\n".join(faq_parts)

